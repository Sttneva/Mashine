public class ConstOnetoFour {
    int age;
    String name;
    double salary;
    String city;


    public ConstOnetoFour(int age) {
        this.age = age;
    }

    public ConstOnetoFour(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public ConstOnetoFour(int age, String name, double salary) {
        this.age = age;
        this.name = name;
        this.salary = salary;
    }

    public ConstOnetoFour(int age, String name, double salary, String city) {
        this.age = age;
        this.name = name;
        this.salary = salary;
        this.city = city;
    }
}
